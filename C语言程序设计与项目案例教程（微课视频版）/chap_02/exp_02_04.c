/*
*   单精度和双精度实型变量的应用
* 
*   样例输出：   3.700000 127869291.925000
*/
#include <stdio.h>

int main(void)
{
    float a, b;                 /* 定义单精度变量a, b */
    double c, d;                /* 定义双精度变量 c, d */
    a = b = 3.7;                /* 变量 a, b 均赋值为 3.7 */
    c = 127869288.225;          /* 变量 c 赋值为 127869288.225 */
    d = a + c;                  /* a + c 的结果赋值给变量 d */

    printf("%f %f", a, d);      /* 分别输出变量 a, d 的值 */

    return 0;
}